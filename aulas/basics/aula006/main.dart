void main(List<String> args) {
  print("Incremento De variáveis");

  print("");

  int n1 = 0;

  print(n1);

  n1 = n1 + 1;

  print(n1);

  n1++;

  print(n1);

  n1 += 1;

  print(n1);

  n1 += 2;

  print(n1);

  n1 += 5;

  print(n1);

  ++n1;

  print(n1);

  print("");

  print("Decremento De variáveis");

  print("");

  n1 = 10;

  print(n1);

  n1 = n1 - 1;

  print(n1);

  n1--;

  print(n1);

  n1 -= 1;

  print(n1);

  n1 -= 2;

  print(n1);

  n1 -= 5;

  print(n1);

}
void main(List<String> args) {
    print("Laços de Repetição | While");

    print("");

    dynamic n1 = 1;

    while(n1 <= 5){
        print("Valor é; $n1");
        n1++;
    }

    print("");

    print("Laço de Repetição | Do While");

    print("");

    do {
        print("valor é $n1");
        n1++;
    }while(n1 <= 10);
}